function repairTerm(term, sectionTitle) {
  const type = sectionType(sectionTitle);
  term.definition = removeBoilerplateDefinition(term.definition);

  if (/в рабочем проекте этот термин фиксируют/i.test(term.example || '')) {
    term.example = defaultExample(term, type);
  }

  if (type === 'tips' && tipOverrides[term.title]) {
    Object.assign(term, tipOverrides[term.title]);
  }

  if (directOverrides[term.title]) {
    Object.assign(term, directOverrides[term.title]);
  }

  if (type === 'tech') {
    term.importance = `${term.title} полезен не сам по себе, а в тех задачах, где нужно управлять способом решения: дать модели примеры, заставить ее думать пошагово, разбить проблему на этапы или подключить самопроверку. Чем сложнее задача, тем заметнее влияние этой техники на точность и устойчивость ответа.`;
  }

  if (type === 'rag') {
    term.importance = `${term.title} важно для систем, которые отвечают по внешним документам, а не только по внутренним знаниям модели. Именно такие элементы определяют, найдет ли система нужный фрагмент, снизит ли число галлюцинаций и сможет ли опираться на актуальные корпоративные данные.`;
    term.extra = `При работе с ${term.title} всегда проверяйте его не в теории, а на реальных документах и реальных вопросах пользователей. В RAG-системах ошибка часто возникает не в самой модели, а на этапе разбиения текста, поиска, ранжирования или сборки финального контекста.`;
  }

  if (type === 'agent') {
    term.importance = `${term.title} нужно понимать при проектировании систем, где модель не просто отвечает текстом, а планирует действия, использует инструменты и принимает промежуточные решения. От этого зависит, насколько агент будет полезным в реальной автоматизации, а не только в красивой демо-сцене.`;
    term.extra = `Для ${term.title} важно проверять не только успешный сценарий, но и сбои: неверные аргументы функции, пустой ответ инструмента, зацикливание шагов или конфликт инструкций. Именно такие проверки показывают, насколько агент пригоден для реальной эксплуатации.`;
  }

  if (type === 'api') {
    term.importance = `${term.title} относится к практической интеграции моделей в продукт. Такие термины нужны, чтобы надежно передавать данные, обрабатывать ошибки, контролировать задержку и строить сценарии, в которых ответ модели становится частью работающего приложения, а не просто отдельного чата.`;
  }

  if (type === 'format') {
    term.importance = `${term.title} важно там, где ответ модели должен быть пригоден для дальнейшей машинной обработки, интерфейса или автоматического пайплайна. Понимание этого термина помогает отличать просто «понятный текст» от действительно надежного структурированного вывода.`;
  }

  if (type === 'safety') {
    term.importance = `${term.title} помогает понять, где именно AI-система может дать опасный, ложный или нежелательный результат и какие меры защиты нужны на уровне архитектуры, а не только одного промпта. Для практической работы это критично, потому что безопасность LLM определяется всей системой целиком.`;
    term.extra = `Термин ${term.title} лучше изучать через сценарии атаки или сбоя: какой вход приходит, где ломается защита и какой слой должен это остановить. Так понятие перестает быть абстрактным и превращается в инженерный риск, с которым можно работать.`;
  }

  if (type === 'tool') {
    term.importance = `${term.title} полезен как инструмент ускорения разработки, но его нужно оценивать не по популярности, а по месту в стеке: что именно он автоматизирует, какие зависимости добавляет и насколько хорошо подходит под ваш сценарий. Это помогает отличать реальную пользу инструмента от модного названия.`;
  }

  if (type === 'training') {
    term.importance = `${term.title} помогает понять, когда промпта уже недостаточно и поведение модели нужно менять через обучение, адаптацию или предпочтения пользователей. Такие термины важны для чтения статей, выбора стратегии дообучения и оценки стоимости реальной адаптации модели под задачу.`;
    term.extra = `Для ${term.title} важно различать цель метода: улучшить следование инструкциям, адаптировать модель под домен, сэкономить память, избежать деградации старых навыков или ускорить эксперимент. Без этого все методы обучения начинают казаться одинаковыми, хотя на практике они решают разные инженерные задачи.`;
  }

  if (type === 'multimodal') {
    term.importance = `${term.title} важно для понимания систем, которые работают не только с текстом, но и с изображениями, документами, аудио или смешанными типами данных. Это знание помогает видеть полный путь обработки информации, а не сводить все к обычному чат-ответу.`;
  }

  if (type === 'eval') {
    term.importance = `${term.title} нужно для оценки моделей на измеримой основе, а не по отдельным красивым ответам. Такие термины помогают понимать, что именно проверяет тест, какие способности он отражает и почему высокий балл по одному бенчмарку не означает универсального превосходства модели.`;
  }

  if (type === 'perf') {
    term.importance = `${term.title} помогает управлять скоростью, стоимостью и пользовательским опытом при работе с AI-системой. Для продукта важно не только качество ответа модели, но и то, насколько быстро этот ответ появляется и сколько ресурсов требует генерация.`;
  }

  if (type === 'infra') {
    term.importance = `${term.title} нужен для понимания того, на какой вычислительной базе реально работают модели и откуда берутся ограничения по памяти, скорости, стоимости и масштабу. Без таких терминов трудно принимать инженерные решения о локальном запуске, серверной инфраструктуре и оптимизации инференса.`;
  }

  if (type === 'task') {
    term.importance = `${term.title} связывает словарь терминов с реальными прикладными сценариями. Такие задачи помогают понять, не только как устроены модели и промпты, но и где именно они приносят практическую пользу в продуктах и автоматизации.`;
  }

  if (type === 'concept') {
    term.importance = `${term.title} относится к общим идеям, которые помогают связать отдельные техники, архитектуры и наблюдения в цельную картину развития LLM. Такие понятия особенно полезны, когда нужно понять не только «что это за термин», но и почему модели ведут себя именно так.`;
  }

  if (type === 'acro') {
    term.definition = clean(term.definition.replace(/Проще всего воспринимать .*?контексте этот акроним обычно появляется\./, 'Это сокращение важно не только расшифровать, но и привязать к области применения: архитектуре моделей, обучению, инфраструктуре, безопасности или прикладным задачам.'));
    term.importance = `${term.title} стоит узнавать мгновенно при чтении статей, документации и технических обсуждений. Для новичка это важно, потому что незнакомые акронимы быстро разрывают понимание текста, даже если сама идея за ними не слишком сложна.`;
    term.extra = `Когда встречаете ${term.title}, полезно сразу задавать себе три вопроса: что означает сокращение, к какому слою AI-системы оно относится и в каком практическом контексте его используют. Такой подход помогает не зубрить буквы, а строить рабочее понимание терминологии.`;
  }

  term.definition = clean(term.definition);
  term.example = (term.example || '').replace(/\r\n/g, '\n').trim();
  term.importance = clean(term.importance);
  term.extra = clean(term.extra);
}

function main() {
  const data = load();
  for (const section of data.sections) {
    for (const term of section.terms) {
      repairTerm(term, section.title);
    }
  }
  save(data);
}

main();
