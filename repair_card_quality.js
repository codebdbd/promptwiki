const fs = require('fs');

const DATA_FILE = 'D:\\prompt_class\\terms-data.js';

function load() {
  const text = fs.readFileSync(DATA_FILE, 'utf8');
  const match = text.match(/window\.DICTIONARY_DATA\s*=\s*(\{[\s\S]*\});?\s*$/);
  if (!match) throw new Error('data not found');
  return JSON.parse(match[1]);
}

function save(data) {
  fs.writeFileSync(DATA_FILE, 'window.DICTIONARY_DATA = ' + JSON.stringify(data, null, 2) + ';\n', 'utf8');
}

function clean(text) {
  return (text || '').replace(/\s+/g, ' ').trim();
}

function removeBoilerplateDefinition(text) {
  return clean(
    (text || '')
      .replace(/ ?По примеру видно, как .*?описании\./g, '')
      .replace(/ ?В рабочем проекте.*$/g, '')
  );
}

function sectionType(title) {
  if (title.includes('Техники промптинга')) return 'tech';
  if (title.includes('RAG')) return 'rag';
  if (title.includes('AI-агенты')) return 'agent';
  if (title.includes('API и интеграция')) return 'api';
  if (title.includes('Форматы данных')) return 'format';
  if (title.includes('Безопасность')) return 'safety';
  if (title.includes('Обучение and Fine-Tuning') || title.includes('Обучение и Fine-Tuning')) return 'training';
  if (title.includes('Мультимодальность')) return 'multimodal';
  if (title.includes('Оценка и метрики')) return 'eval';
  if (title.includes('Производительность')) return 'perf';
  if (title.includes('Инфраструктура')) return 'infra';
  if (title.includes('Фреймворки')) return 'tool';
  if (title.includes('Специализированные задачи')) return 'task';
  if (title.includes('Дополнительные концепции')) return 'concept';
  if (title.includes('Практические советы')) return 'tips';
  if (title.includes('Сокращения и акронимы')) return 'acro';
  return 'generic';
}

